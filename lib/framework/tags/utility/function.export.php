<?php
/**
 * Defines the export smarty tag, which outputs the result of an export method call
 *
 * <b>Parameters:</b><br />
 * var - The variable to call export on
 *
 * @package RabbitPHP_Tags
 * @subpackage Utility
 * @author Kerry R Wilson <kerry@rabbitphp.org>
 * @version 0.1
 */
 function smarty_function_export($params, &$smarty)
 {
     return var_export($params['var'],true);
 }
?>